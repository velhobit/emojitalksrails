# app/controllers/posts_controller.rb
class PostsController < ApplicationController
  before_action :set_post, only: %i[show update destroy]

  # GET /posts
  def index
	@posts = Post.all
	render json: @posts
  end

  # GET /posts/:id
  def show
	render json: @post
  end

  # POST /posts
  def create
	@post = Post.new(post_params)
	if @post.save
	  render json: @post, status: :created, location: @post
	else
	  render json: @post.errors, status: :unprocessable_entity
	end
  end

  # PATCH/PUT /posts/:id
  def update
	if @post.update(post_params)
	  render json: @post
	else
	  render json: @post.errors, status: :unprocessable_entity
	end
  end

  # DELETE /posts/:id
  def destroy
	@post.destroy
  end

  private

  def set_post
	@post = Post.find(params[:id])
  end

  def post_params
	params.require(:post).permit(:uuid, :title, :content, :forum_id, :author_id, :closure_date, :deletion_date)
  end
end